package com.forloop;

public class Task_3 {

	public static void main(String[] args) {
		System.out.println("For Loop");
				for (int i = 70; i <= 90; i++) {
					System.out.print(i+ ", ");
				}
			System.out.println();
			
			System.out.println("While Loop");
			int j = 70;
			
			while (j<=90) {
				System.out.print(j+ ", ");
				j++;
			}
		System.out.println();
			
			System.out.println("Do While Loop");
			
			int k = 70;
			do {
				System.out.print(k+ ", ");
				k++;
				
			} while (k<=90);
			
}
	}
